package tp3.ej1;

public class Pregunta {

    private String consigna;
    private int nroPregunta;

    public Pregunta(String consigna, int nroPregunta) {
        this.consigna = consigna;
        this.nroPregunta = nroPregunta;

    }

    public int getNroPregunta() {
        return nroPregunta;
    }

}